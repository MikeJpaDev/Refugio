    private void actualizarCmbContratos() {
        DefaultComboBoxModel<String> model = (DefaultComboBoxModel<String>) contCmb.getModel();
        model.removeAllElements();
        contratos = ContratoService.getAllContratosByProveedor(proveedor.proveedor_id());
        for (ContratoTable cont: contratos){
            model.addElement(cont.descripcion());
        }
        DefaultComboBoxModel<String> modelServ = (DefaultComboBoxModel<String>) servCmb.getModel();
        modelServ.removeAllElements();
        ContratoTable cont = contratos.get(contCmb.getSelectedIndex());
        servicios = ServicioService.getAllServicioByContrato(cont.contratoId(), cont.proveedorId());
        for (Servicio serv: servicios){
            modelServ.addElement(serv.tipoServicio());
        }
        System.out.println("kuu");
    }

    private void actualizarCmbServ() {
        DefaultComboBoxModel<String> model = (DefaultComboBoxModel<String>) servCmb.getModel();
        model.removeAllElements();
        ContratoTable cont = contratos.get(contCmb.getSelectedIndex());
        servicios = ServicioService.getAllServicioByContrato(cont.contratoId(), cont.proveedorId());
        for (Servicio serv: servicios){
            model.addElement(serv.tipoServicio());
        }
    }

