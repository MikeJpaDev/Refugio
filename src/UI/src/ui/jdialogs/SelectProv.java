    private void llenarTabla(){
        limpiarTabla();
        listaAux = Filters.provFilter(lista, nombreTxt.getText(), "", "", "");
        for (Proveedor prov : listaAux) {
            String tipo = "Desconocido";
            if(prov instanceof Veterinario)
                tipo = "Veterinario";
            else if(prov instanceof ProveedorAlimento)
                tipo = "Alimento";
            else if(prov instanceof ProveedorComplementario)
                tipo = "Complementario";
            tableModel.agregarProveed(prov.nombre_proveedor(), prov.provincia(), tipo, prov.responsable());
        }
    }

    private static void limpiarTabla(){
        int cantFil = tableModel.getRowCount()-1;
        for(int i=cantFil ; i>=0 ; i--){
            tableModel.removeRow(i);
        }
    }

