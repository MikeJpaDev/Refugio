    private void aceptarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarBtnActionPerformed
        try {
            if(animTxt.getText().isEmpty()){
                throw new IllegalArgumentException("Seleccione un animal");
            }
            if(fechaTxt.getText().isEmpty()){
                throw new IllegalArgumentException("Seleccione una fecha");
            }

            Double precio = (Double) precioSpin.getValue();
            
            AdopcionService.createAdopcion(animal.animal_id(), fecha, precio);

            this.hide();
            JOptionPane.showMessageDialog(null, "Creado Correctamente", "Creado Satisfactoriamente", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }
        catch (IllegalArgumentException e){
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error de validaci√≥n", JOptionPane.ERROR_MESSAGE);
        }
        catch (SQLException e1){
            e1.printStackTrace();
            String mensajeError = "Error de base de datos: ";

            JOptionPane.showMessageDialog(null, mensajeError, "Error de base de datos", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_aceptarBtnActionPerformed

